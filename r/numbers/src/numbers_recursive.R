fibonacci <- function(n) {
  if (n <= 0) {
    return(0)
  } else if (n <= 2) {
    return(1)
  } else {
    return(fibonacci(n - 1) + fibonacci(n - 2))
  }
}

factorial <- function(n) {
  if (n <= 1) {
    return(1)
  } else {
    return(n * factorial(n - 1))
  }
}

sum_numbers <- function(n) {
  if (n <= 0) {
    return(0)
  } else {
    return(n + sum_numbers(n - 1))
  }
}